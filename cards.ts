interface Card {
    name: string;
    srcFront: string;
    srcBack: string;
}

export function getRandomCardsArray(quantityCards: number): Card[] {
    let sixCardsArray: Array<object> = [];
    for (let i = 0; i < quantityCards; i++) {
        while (sixCardsArray.length < quantityCards*2) {
            let randomCard = getRandomCard(AllCards);

            if (!sixCardsArray.includes(randomCard)) {
                sixCardsArray = sixCardsArray.concat(dublicateCards(randomCard, 2));
            } else {
                randomCard = getRandomCard(AllCards);
            }
        }
    }
    return sixCardsArray;
}

function getRandomCard(arr: Array<object>) {
    return arr[Math.floor(Math.random() * arr.length)];
}

function dublicateCards(card: object, n: number) {
    let newArray = [];
    for (let i = 0; i < n; i++) {
        newArray.push(card);
    }
    return newArray;
}